        votre crayon{{'\xa0'}}?{{'\xa0'}}»{{'\xa0'}}; «{{'\xa0'}}- File moi ton crayon{{'\xa0'}}»{{'\xa0'}};  «{{'\xa0'}}- Peux tu me passer 
        ton crayon{{'\xa0'}}?{{'\xa0'}}»
        </li>
       </ul>
     </li>
   </ul>
 </p>
