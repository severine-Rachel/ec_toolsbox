      </h3>
      <p>
        Premier cours de littérature dans saprépa toulousaine. Julie (le pré­nom
        a été modifié) reste muette. Elle observe ébahie ses camarades prendre
        la parole et s’exprimer sur l’œuvre étudiée. «
        <i>
          Ils m’impressionnaient par leurs connaissances. Devant leurs phrases
          si bien construites, je me suis dit : “Des gens sa­vent vraiment
          parler comme ça ?” Les mots qu’ils employaient voulaient dire tout ce
          que je ressentais et que je n’arrivais pas à nommer. </i
        >» Julie a grandi dans un petit village, à deux heu­res de la capitale
        occitane. Ses parents ont connu, enfants, la grande précarité, et Julie
        est la première de la famille à arriver jusqu’au bac.Porté par la
        dynamique des « trentes glorieu­ses », son père a monté son imprimerie
        et connu une ascension sociale. Aujourd’hui, la famille vit
        confortablement mais, à table, on ne parle ni littérature, ni cinéma, ni
        histoire. Quand Julie, bonne élève au lycée, choisit d’al­ler en prépa
        littéraire, elle est loin d’imaginer lechoc qui sera le sien au contact
        de ses nou­veaux camarades. «
        <i
          >Je me suis vite sentie en dé­calage. Beaucoup venaient de familles de
          pro­fesseurs, ils avaient grandi en écoutant France Inter, étaient
          allés plusieurs fois au musée et avaient beaucoup de connaissances
          politiquesou historiques qui m’étaient inconnues.</i
        >
        »
      </p>
      <p>
        Porté par la dynamique des « trentes glorieu­ses », son père a monté son
        imprimerie et connu une ascension sociale. Aujourd’hui, la famille vit
