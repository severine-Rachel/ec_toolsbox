      <p>
        De nombreux étudiants ressentent un telmalaise en entrant dans
        l’enseignement su­périeur, lorsqu’ils se confrontent à des jeunes ayant
        bénéficié, par le biais de leur famille et de leur entourage, d’un
        important capital so­cial et culturel. Ce sentiment de « manquer
        deculture générale » culmine lorsque celle­ci faitl’objet d’une épreuve
        écrite ou orale pour inté­grer une grande école, un institut d’études
        po­litiques ou un concours administratif.
      </p>
      <p>
        C’est au début du XXe siècle que la culturegénérale devient une épreuve
        de concours – d’abord pour les écoles militaires – quidépartage les
        candidats sur leur «<i> hauteur de vue et la sûreté de leur jugement </i
        >», décriventles chercheurs Charles Coustille et Denis Ramond, dans un
        article de la revue <i>Le Débat</i>. Les compositions de culture
        générale se mul­tiplient au milieu du siècle, notamment dans les
        concours menant à la haute administra­tion, dans les écoles de commerce.
      </p>
      <p>
        «
        <i
          >Ces épreuves évaluent le “capital culturel in­corporé” : il s’agit de
          maîtriser tout un ensembledisparate de connaissances et, surtout, de
          sa­voir les mettre en scène,</i
        >
        explique Annabelle Al­louch, maîtresse de conférences en sociologie à
        l’université de Picardie­Jules­Verne, auteure de La Société du concours
        (Seuil, 2017).<i>
          L’effet est d’identifier le milieu social du candidat,et cequi
          pourrait faciliter son adhésion au corps de l’école, de
          l’administration. Les jurys se deman­dent : ce candidat pourra­t­il
          parler des mêmes choses que moi à midi ? Cette logique du mimé­tisme
          est très discriminante, et c’est en cela que la sociologie considère
          la culture généralecomme l’épreuve par essence des héritiers. </i
        >»
      </p>
      <h4>« Trivial Pursuit »</h4>
      <p>
        Fille d’immigrés chinois arrivés à Paris dans les années 1980, Sonia a
        étudié dans un lycée privé du 20e arrondissement. Ses parents par­lent
        peu le français. Aux côtés de ses camara­des de lycée, elle sentait déjà
        un différentiel deréférences et d’aisance pour évoquer des su­jets
        d’actualité, parler d’histoire ou d’œuvres d’art. «<i>
          C’est quand on faisait des jeux, comme le Trivial Pursuit, que je me
          rendais compte queje ne connaissais vraiment rien. C’était humi­liant. </i
        >» Elle avait aussi du mal à prendre part àcertains sujets de
        discussion, et se contentait «<i> la plupart du temps d’acquiescer </i
        >».Cela ne l’empêche pas de poursuivre ses études.En 2015, après un
        master d’histoire à l’univer­sité Paris ­Sorbonne, elle décide de
        préparer les concours des écoles de journalisme et intè­gre la prépa La
        Chance. Gratuite, cette der­nière aide les étudiants boursiers,
        notam­ment en matière de culture générale. «
        <i
          >J’avais l’impression que c’était une montagne à soule­ver, confie
          Sonia. Après un master, je pensais avoir les bases, mais je me suis
          rendu compte que la culture générale ne recouvrait pas ces sa­voirs
          communs appris à l’école, mais plutôt ceque la famille peut nous
          amener à connaître. </i
        >» Son année de bachotage ne l’a pas empê­chée de se retrouver démunie
        face à certainesépreuves des concours des écoles de journa­lisme. «
        <i>Le concept de culture générale est très cloisonné </i>»,
        regrette­t­elle aujourd’hui. «<i>
          J’ai mis du temps à réaliser que tout ce que j’avais comme
          connaissances était aussi de la culture,mais que celle­ci n’était pas
          admise dans les concours.</i
        >
        » La culture générale, une forme de « culture officielle » de la
        bourgeoisie ? «
        <i
          >C’est une notion élastique dont on s’est servi pourentretenir un
          certain entre­soi </i
        >», regrette le président de La Chance, Marc Epstein.
      </p>
      <p>
        Conscientes de l’aspect discriminant de cesépreuves, de nombreuses
        écoles, dont Scien­ces Po, les ont supprimées de leurs concours.«
        <i
          >Mais, dans les faits, il s’agit plus d’une reconfi­guration que d’une
          disparition,
        </i>
        Anna­belle Allouch.<i>
          L’identification du capital cultu­rel perdure dans presque toutes les
          épreuves, où ces connaissances et la manière de les resti­tuer sont
          toujours valorisées. Cela ne change donc rien au sentiment
          d’illégitimité que peu­vent connaître certains élèves.</i
        >
        »
      </p>
      <p>
        Le « malaise » de la culture générale ne selimite pas aux concours et
        aux dissertations. Il est aussi vif au quotidien, dans les interac­tions
        avec professeurs et camarades, et se révèle plus répandu qu’il n’y
        paraît chez lesétudiants. D’autant que, selon Annabelle Allouch, il ne
        naît pas uniquement du man­que de connaissances culturelles. «
        <i
          >Nous nous focalisons sur la notion de culture générale car c’est l’un
          des traits sociaux les plus visibles. Mais le sentiment d’illégitimité
          – exprimé comme “je n’ai pas assez de culture” – est en fait souvent
          le reflet de multiples autres boulever­sements associés à l’arrivée
          dans un nouveau milieu </i
        >», postule la sociologue.
      </p>
      <h4>
        Multiples ruptures
      </h4>
      <p>
        Ces multiples ruptures, Julie les a ressenties. Elle sentait ses
        formulations maladroites. Elle évitait de parler de ses parents,
        notamment des convictions politiques de droite de sonpère, taboues pour
        elle face à un milieu où beaucoup votent à gauche et l’affichent
        claire­ment. «
        <i
          >A 20 heures, mes parents regardent TF1, ajoute l’étudiante en master
          de géopolitique de 23 ans. Or je me suis rendu compte que, pour les
          gens en prépa, cette chaîne était nulle, trop populaire. J’ai eu un
          intense sentiment dehonte de me dire que j’avais grandi avec. »</i
        >
      </p>
      <p>
        Cette «<i> honte</i> » peut s’accroître avec les an­nées, à mesure que
        l’étudiant avance dans ses études. A chaque étape de son cursus, Aimée
        connaît un nouveau choc. Le premier se fait enclasse prépa. Née dans les
        Vosges, elle grandit dans une famille protestante. Une mère aufoyer, un
        père bûcheron. «<i>
          Dans tous les textes qu’on étudiait en prépa, je saisissais les
          référen­ces bibliques – j’étais d’ailleurs la seule – mais je ne
          connaissais rien d’autre. C’était très violentde se rendre compte de
          tout ce que je devais rat­traper », confie l’étudiante de 23 ans. Son
          arri­vée à l’Ecole normale supérieure de Paris (ENS)est une « nouvelle
          claque</i
        >
        »
      </p>
      <p>
        «
        <i
          >Je n’avais pas les codes, je me sentais comme la prolo ignorante
          montée à la capitale. </i
        >» Que ce soit en cours ou dans ses interactions avec ses amis et ses
        profs, Aimée ne se sent « pas légi­time ». «
        <i
          >Un jour, nous avions organisé un verre à l’ENS, se souvient­ elle.
          Une prof, à qui j’avais fait part de mes difficultés, était venue me
          de­mander : “Alors, Aimée, ça va mieux avec Bour­dieu ?”. Je ne
          comprenais toujours pas cet auteur, alors je n’ai pas su quoi lui
          répondre. Pour elle, parler de Bourdieu représentait une conversation
          banale, qu’on pouvait avoir de manière décontractée autour d’un verre.
          Pour moi, c’était un casse­ tête. </i
        >» Peu à peu, Aimée « <i>rattrape </i>[son] <i>retard</i> », elle note
        ce dont par­lent ses camarades dans la journée et va faire des
        recherches le soir sur Internet. «
