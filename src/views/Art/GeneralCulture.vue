      <h2>À QUOI SERT LA CULTURE GÉNÉRALE ?</h2>
      <h3>
        <strong>ENQUÊTE</strong> Un pamphlet s’interroge sur les bienfaits de la
        culture. Nous avons voulu savoir ce qu’en pensent les écrivains
      </h3>
      <p>
        EST-IL indispensable d’être cultivé ? Cette question paraît presque
        indécente en France où l’on fait si grand cas de la culture générale.
        Elle est posée par un professeur de philosophie de l’université du
        Québec, dans un petit pamphlet qui a le mérite de nous faire réfléchir à
        ce qu’on attend de la culture. L’étranger de passage, raconte Normand
        Baillargeon, avisant les milliers de manuels de culture générale qui
        s’étalent dans les librairies françaises, se demande pourquoi on lui
        accorde tant d’importance. Il s’amuse aussi de voir qu’un ministre qui a
        commis un lapsus en citant une œuvre de Voltaire est cloué au pilori
        comme s’il avait commis un péché grave. Qu’on se souvienne aussi du
        tollé qu’a suscité le président de la République lorsqu’il a dit qu’ «<i>
          avoir fait quinze ans de bénévolat valait autant que de savoir par
          cœur</i
        >
        La Princesse de Clèves » .
      </p>
      <p>
        L’écrivain Xavier Patier rappelle que cette passion française ne date
        pas d’hier. Dans <i>La Guerre des Gaules</i>, Jules César écrit que les
        Gaulois sont un peuple d’avocats qui a besoin de parler et qui aime les
        idées générales : que les Avernes sont comme ci et les Ruthènes comme
        ça… Mais à quoi sert-elle, cette culture très générale? La romancière
        Laurence Cossé témoigne que des amis étrangers, de pays européens
        pourtant éminents,lui disent quelque fois leur étonnement de voir qu’en
        France on a facilement des conversations de fond avec le garagiste, la
        boulangère ou le facteur, bien sûr à partir de l’actualité, mais pas
        seulement, sur des événements culturels, historiques, sur le patrimoine,
        etc.
      </p>
      <p>
        Selon Xavier Patier, «<i>la culture générale, c’est la mémoire</i>», une
        mémoire commune qui crée du lien social. «<i
          >La laïcité, c’est très bien, ça crée de la liberté mais pas du lien,
          comme le faisait la religion. </i
        >» Il compare le décret d’attribution de Malraux, chargé de «
        <i>permettre à tous les Français d’accéder aux trésors de la culture </i
        >», à celui de Jack Lang, qui avait pour mission de «
        <i
          >permettre à tous les Français d’exprimer leur talent et leur
          créativité </i
        >», ce qui n’a rien à voir. Patier commente : «
        <i>Quand l’État méprise la culture générale, il sépare les gens. </i>»
      </p>
      <p>
        Le romancier et essayiste François Taillandier pense lui aussi «
        <i
          >qu’il est bon que le plus grand nombre possible de gens aient les
          mêmes références, que chacun sache, même un peu vaguement, qui étaient
          Clovis ou Charlie Chaplin, ce qu’est le cartésianisme et ce qu’il y a
          dans l’Évangile </i
        >». À cet égard, il était « <i>hhhîndigné </i>», comme il dit, qu’on ait
        retiré l’épreuve de culture générale de certains concours administratifs
        : «
        <i
          >Mon postier a le droit d’avoir vu le David de Michel-Ange ou lu un
          roman de Zola. Non seulement il en a le droit, mais c’est utile à la
          société. Il me semble intéressant qu’il partage des choses avec moi :
          on vit ainsi dans le même monde. </i
        >»
      </p>
      <h4>
        «Humanités modernes»
      </h4>
      <p>
        Ce n’est pas l’avis d’un autre grand écrivain, Gaspard-Marie Janvier,
        résolument opposé au « <i>tout un peu</i> ». Les tests de culture
        générale, affirme-t-il, ressemblent de plus en plus à des quiz qui
        exigent seulement qu’on connaisse les dates de naissance des auteurs, ce
        qui n’a pas grand intérêt si on n’a aucune idée de ce qu’ils écrivent.
        GMJ fut chercheur en mathématiques avant de devenir professeur de
        lettres en khâgne. Il a aussi enseigné la culture générale en prépa HEC
        et en maths sup.La seule façon de le faire correctement,
        explique-t-il,c’est de suivre un thème - le temps,laliberté,ledésir…en
        montrant à ses élèves en quoi la culture littéraire, philosophique,
        scientifique, religieuse peut l’éclairer. «
        <i
          >La culture n’est pas une notion bien-pensante. C’est très concret: il
          s’agit d’être capable de se servir de l’expérience de ceux qui ont
          réfléchi, senti, aimé, ri, pleuré, avant moi, afin de mieux répondre
          aux situations de la vie. </i
        >» Xavier Patier le formule autrement : «
        <i
          >La culture consiste à transformer le maximum de connaissance
          enmaximumdeconscience. </i
        >»
      </p>
      <p>
        Gaspard-Marie Janvier rappelle l’origine agricole du mot «
        <i>culture</i> ». Se cultiver, c’est labourer sa nature terreuse
        pourl’ouvrir à la lumière, à l’eau du ciel et l’ensemencer de
        connaissances. «<i>
          Si je remue mal ma terre, si je me contente d’y jeter des semences
          sans la travailler en profondeur, j’aurai des pommes de terre
          racornies et infectes !</i
        >
        »
      </p>
      <p>
        Au terme « culture », devenu un fourre-tout, il préférerait celui d’«
        humanités modernes », qui comprendrait les sciences et les techniques.
        Car la physique et les maths, c’est aussi de la culture et pas des
        recettes de cuisine à appliquer. Dommage qu’on ne les enseigne pas comme
        telles à l’école: «<i>
          C’est en racontant comment Archimède dessinait ses triangles et
          quelles furent les conséquences de ses découvertes sur l’histoire de
          l’humanité qu’on y intéressera les enfants.</i
        >
        »
      </p>
      <p>
        Un mystère demeure. Les gens cultivés ne sont ni plus vertueux, ni même
        forcément plus clairvoyants que les autres. «
        <i
          >C’est la faute au péché originel ! commente Gaspard Marie Janvier, la
          tendance de l’homme à instrumentaliser ses compétences pour sa propre
          gloire. </i
        >» Il y a des cuistres à courte vue et de grands érudits qui sont des
        sales types. «
        <i
          >Si la culture était la panacée, ça se saurait. PolPot a fait une
          thèse sur Verlaine </i
        >», poursuit l’auteur du <i>Dernier Dimanche</i>. On pense aussi à ce
        mot de saint Bernard de Clairvaux, grand théologien : «
        <i>On apprend plus de choses dans les bois que dans les livres. </i>»
      </p>
      <h4>Liberté d’esprit</h4>
      <p>
        Laurence Cossé cite une anecdote éloquente. Il y a quelques mois, dans
        une interview publiée dans un quotidien du soir, une universitaire
        américaine expliquait que le mot anglais <i>pic-nic</i> vient de
        <i>picking the negroes</i>, autrement dit des expéditions punitives
        contre les Noirs au XIXe siècle. En réalité, le mot « pique-nique » date
        du XVIIe siècle et vient de « piquer » et de « nique », qui veut dire «
        petite chose sans valeur ». Laurence Cossé en conclut qu’il est
        nécessaire d’avoir une bonne culture générale pour ne pas se laisser
        abuser par ce genre d’informations erronées. On constate aussi que des
        gens très cultivés comme des universitaires peuvent être aveuglés par
        l’idéologie.
      </p>
      <p>
        «
        <i
          >La culture, reconnaît Taillandier, n’a rien à voir avec
          l’intelligence. Il y a des gens qui ont une liberté d’esprit sans
          rapport avec leur niveau d’études. </i
        >» Certaines personnes pâtissent même d’une « surcharge culturelle ». Il
        pense à quelques confrères écrivains dont le talent a été étouffé par un
        excès de références savantes. «<i>
          Il est bon d’oublier. Quand on est surcultivé, on est encombré. On
          perd une forme de naïveté. </i
        >»
      </p>
      <p>
        De la culture, comme de l’argent ou de la force, on peut faire bon ou
        mauvais usage. Avoir de la culture, ou être cultivé, là est la question.
        Pour filer la métaphore agricole, c’est la même différence qu’entre
        stocker du grain dans un grenier à blé ou le semer dans un champ bien
        labouré.
      </p>
