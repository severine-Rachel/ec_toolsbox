      <h2>La culture générale des salariés doit devenir une priorité</h2>
      <p>
        Aujourd'hui, en entreprise, une fois arrivé à un certain niveau de responsabilité, ce sont moins les compétences métiers ou techniques qui fondent le succès (de fait, la plupart des collaborateurs les possèdent) qu'un ensemble de compétences « invisibles », souvent appelées « soft skills ». Posséder un savoir-être spécifique; être à l'aise et intéressant à l'oral lors d'un dîner avec un client; savoir débattre avec des interlocuteurs de haut niveau... telles sont par exemple des qualités qui font toute la différence entre deux collaborateurs de même formation et de même expérience.
      </p>
      <p>
        La maîtrise des codes sociaux est un plafond de verre dont on ne parle presque jamais. La culture est l'un des facteurs discriminants les plus redoutables : difficilement quantifiable et aux frontières floues, elle n'est pas évaluée puisqu'elle n'occupe aucune place officielle dans le monde de l'entreprise, censé être celui de la rationalité économique. Pourtant, elle confère à celui qui la possède une précieuse confiance en soi. Comme l'avait montré Bourdieu, la culture est un outil essentiel de distinction (dans les deux sens du terme), discriminant de façon d'autant plus insidieuse que ceux qui ne la possèdent pas sont inconscients du subtil classement qu'elle opère.
      </p>
      <p>
        On le sait, notre bagage culturel dépend du cadre socio-économique dans lequel nous évoluons et avons grandi. Les différences qui en résultent peuvent jouer dès l'entretien d'embauche (par exemple, remarquer et commenter l'oeuvre qui trône dans le bureau de son futur employeur), essentiellement, il est vrai, pour l'accès à des fonctions élevées de direction. Avec la digitalisation de la société, la culture générale va devenir plus cruciale que jamais. Préemptant toutes les tâches mobilisant de simples capacités d'application, la robotisation n'épargnera que les emplois faisant appel à la création, l'inventivité et la plasticité; autant de compétences qui trouvent leur source dans la culture générale.
      </p>
      <p>
        Soucieuses de démontrer qu'elles s'inscrivent dans une démarche d'équité et qu'elles sont conscientes de leur responsabilité sociétale, les entreprises cherchent à remettre en question les différents « plafonds de verre » empêchant la juste progression de leurs collaborateurs. Mais la culture est encore trop souvent vue comme un simple gadget par les entreprises. Pourtant, elles auraient tout à gagner à prendre la culture de leurs collaborateurs au sérieux. Ce n'est pas juste une histoire d'image : les entreprises recrutent et réalisent des promotions sur ces critères. Elles écartent donc inconsciemment une partie des candidats potentiels (les meilleurs peut-être !), sans leur proposer de solution pour se préparer et se mettre à niveau. Elles ratent ainsi de nombreux collaborateurs à fort potentiel qui n'ont pas eu la chance de bénéficier d'un environnement favorable. Les initiatives donnant une vraie place à la culture en entreprise et cherchant à la développer chez les collaborateurs sont encore rares.
      </p>
      <p>
        La culture générale n'arrive pas par magie, elle n'est pas non plus condamnée à n'être qu'un patrimoine hérité; elle s'apprend, et cela à tout âge. Or, il existe peu de dispositifs permettant de se mettre à niveau rapidement, simplement et de façon décomplexante, contrairement aux multiples solutions visant à améliorer ses compétences linguistiques, dans un métier, ou même managériales. Des outils efficaces pourraient pourtant être facilement développés, pour peu que des entreprises souhaitent réellement en faire une priorité du développement de leurs ressources humaines.
      </p>
      <p>
        Entendue non comme somme des connaissances mais comme disposition à la curiosité, la culture générale doit s'affirmer comme une priorité des entreprises, notamment en tant que facteur d'intégration, d'amélioration de la performance et du bien-être des collaborateurs.
      </p>
