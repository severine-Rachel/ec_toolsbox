      <h2>
        La culture générale, ce trésor

      </h2>
      <h3>
        Soupçonnée d’avantager  les enfants des milieux favorisés,  elle disparaît de l’enseignement supérieur. Et pourtant...

      </h3>
      <p>
        En juin 2013, Sciences po rejoignait l’Ecole normale supérieure de Lyon en supprimant l’épreuve de culture générale de son concours d’entrée. En 2020, l’ENA s’y met aussi. Depuis Pierre Bourdieu, il est de bon ton  de se boucher le nez devant ce qui constituait le ﬂeuron  des humanités. La principale raison invoquée tient  de la discrimination : les enfants de milieux défavorisés  ne bénéﬁcieraient pas des mêmes chances que les ﬁlles  et ﬁls de bourgeois. Sans parler de ceux issus de l’exil. Pourquoi considérer qu’un enfant défavorisé et/ou immigré ne peut acquérir une culture générale ? Pourquoi niveler par le bas alors que le moteur de l’ascenseur social a toujours été l’accès au savoir ?
      </p>
      <h4>
        Le pouvoir, c’est la connaissance

      </h4>
      <p>
        La première fois que je me suis retrouvée devant  une bibliothèque impressionnante chez les parents de mes amis lycéens, j’ai surtout été choquée par la quantité  de poussière – et le nombre de bibelots – qui recouvraient les livres, et par le manque de curiosité des adolescents vivant au milieu de tant de chefs-d’œuvre. Nous n’avions pas l’espace et encore moins les moyens de nous constituer une bibliothèque, mes parents et moi.  Les livres, nous les empruntions à la bibliothèque municipale. Ils ne prenaient jamais la poussière,  ils vivaient avec nous. Posséder une bibliothèque  et des parents cultivés peut vous assurer un avenir, certainement pas une culture. Dans un pays comme la France  où les bibliothèques municipales sont pléthores, où les musées nationaux sont gratuits jusqu’à 25 ans – ainsi que pour  les demandeurs d’emploi et les titulaires des minima sociaux  (dont les réfugiés) –, parler de discrimination est pour le moins absurde. La question est de savoir pourquoi il n’y a plus de jeunes dans les bibliothèques et les musées, mais davantage de retraités  et de touristes, et de trouver les outils pour y remédier. Peut-être  en rappelant que le pouvoir, c’est la connaissance. Peut-être  en martelant qu’étudier mène toujours quelque part – malgré les faiseurs de crétins, confortablement installés dans leur réussite  et leurs acquis, qui en répètent l’inutilité. Peut-être en observant  la culture française depuis l’étranger, où Victor Hugo, les Lumières ou Picasso sont des références qui inspirent et libèrent. Peut-être en cessant de confondre le savoir et le bachotage, en forgeant  les esprits à l’analyse critique et à la confrontation d’idées.

      </p>
      <h4>
        Réussir, c’est trahir

      </h4>
      <p>
        Une étude menée sur un panel de 30 000 élèves durant dix ans  par Yaël Brinbaum (Cnam) nous apprend, en janvier 2020, que les enfants d’origine asiatique réussissent mieux leurs études que ceux  de parents français, alors que les ﬁls et ﬁlles de parents turcs arrivent en dernière place du classement. Les enfants issus du continent asiatique ne bénéﬁcient évidemment pas des mêmes chances que ceux issus des classes favorisées. Mais ils sont poussés par la farouche volonté des parents de leur assurer un avenir et par un respect ancestral de la ﬁgure du professeur. Si les enfants d’origine turque réussissent moins bien, cela relève d’une crainte d’acculturation. Réussir sous-entend s’éloigner de la cellule familiale, du quartier,  de la communauté. Réussir, c’est trahir. La question n’est donc pas  la capacité des enfants à apprendre, mais la peur de perdre culturellement son enfant et de ne plus parler le même langage que lui.

      </p>
      <h4>
        L’esprit critique : un outil précieux 

      </h4>
      <p>
        La culture générale n’est pas une baguette magique.  Mais si le ressenti a pris le pas sur la réalité et le sentiment  sur la raison, si la violence a gangrené le débat d’idées, c’est que nous  ne possédons plus une base commune de savoirs et de références pour échanger sereinement. La culture générale est l’opposé  du roman national, elle n’est pas la culture de l’oppresseur  mais le langage de la liberté, qui, loin de creuser les inégalités,  les corrige plutôt. La France m’a donné une langue, je lui ai oﬀert  des livres. Et cela n’aurait pas été possible sans la culture générale  que j’ai reçue en partage. Elle m’a permis, dans le même mouvement,  de me reconnaître dans la France et de m’en libérer,  en m’oﬀrant un outil précieux : l’esprit critique.
      </p>
      <p>
        Quoi qu’il puisse arriver à la France, quels que soient sa gouvernance et ses présidents, il restera ce socle commun, disons-le sans frémir, cet éventail de valeurs fait d’Histoire, de littérature, de sciences molles et dures, de géographie, d’héroïsme et de défaites.  S’il fallait se lever pour défendre la patrie, il ne serait question  pour moi, allergique à toute forme de nationalisme, de me battre pour des frontières ou des chefs autoproclamés, mais pour  ces valeurs qui nous rappellent, comme le dit l’adage latin,  que « rien de ce qui est humain ne nous est étranger. » 
